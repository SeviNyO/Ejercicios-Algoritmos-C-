#include <iostream>

using namespace std;

int main () {

    //Tipos de variables

    //Variable para números enteros
    int entero = 14;

    // Variable para números decimales pequeños
    float flotante = 3.14;

    //Variable para números decimales grandes
    double doble = 1.77779989;

    //Almacenar letras, dígitos y otros caracteres individuales
    char letra = 'B';

    //Variables BOOLEANAS, almacena TRUE or FALSE
    bool si = true;

    //Almacenar texto
    string nombre = "Sammy";

    // sacar datos, textos

    cout << entero << endl << flotante << endl << doble << endl << letra << endl << si << endl << nombre;

    return 0;
}